    private void cariTransaksi(String kode_transaksi) {
        DefaultTableModel model = (DefaultTableModel) tbl_transaksi.getModel();
//        DefaultTableModel model = new DefaultTableModel();
        model.setRowCount(0);
        
        
        try (Connection connection = koneksi.getConnection()) {
            String query = "SELECT * FROM transaksi WHERE kode_transaksi = ?";
            try (PreparedStatement preparedStatement = connection.prepareStatement(query)) {
                preparedStatement.setString(1, kode_transaksi);
                ResultSet resultSet = preparedStatement.executeQuery();
                System.out.println("Executing query for kode_transaksi: " + kode_transaksi);

                while (resultSet.next()) {
                    String IdTransaksi = resultSet.getString("id_transaksi");
                    String kodetransaksi = resultSet.getString("kode_transaksi");
                    String tanggal = resultSet.getString("tanggal_transaksi");
                    String kodeBarng = resultSet.getString("kode_barang");
                    String namaBarang = resultSet.getString("nama_barang");
                    String Qty = resultSet.getString("jumlah_barang");
                    String total = resultSet.getString("harga_total");

                    Object[] data = {IdTransaksi, kodetransaksi, tanggal, kodeBarng, namaBarang, Qty, total};
                    model.addRow(data);
                }
                System.out.println("Number of rows retrieved: " + model.getRowCount());

                tbl_transaksi.setModel(model);

            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

