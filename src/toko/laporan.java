    private DefaultTableModel getDataTransaksi() throws SQLException {
        Connection connection = koneksi.getConnection();

        if (connection != null) {
            try {
                String query = "SELECT id_transaksi, kode_transaksi, tanggal_transaksi, kode_barang, nama_barang, jumlah_barang, harga_total FROM transaksi";
                try (PreparedStatement preparedStatement = connection.prepareStatement(query)) {
                    ResultSet resultSet = preparedStatement.executeQuery();

                    String[] kolomTabel = {"ID Transaksi", "Kode Transaksi", "Tanggal", "Kode Barang", "Nama Barang", "Qty", "Harga Total"};
                    DefaultTableModel model = new DefaultTableModel(kolomTabel, 0);

                    while (resultSet.next()) {
                        Object[] dataBaris = {
                                resultSet.getString("id_transaksi"),
                                resultSet.getString("kode_transaksi"),
                                resultSet.getString("tanggal_transaksi"),
                                resultSet.getString("kode_barang"),
                                resultSet.getString("nama_barang"),
                                resultSet.getString("jumlah_barang"),
                                resultSet.getString("harga_total")
                        };
                        model.addRow(dataBaris);
                    }

                    return model;
                }
            } finally {
                try {
                    connection.close();
                } catch (SQLException e) {
                    e.printStackTrace();
                }
            }
        }

        return new DefaultTableModel();
    }

    private void hitungTotalHarga() {
        DefaultTableModel model = (DefaultTableModel) tbl_transaksi.getModel();
        int rowCount = model.getRowCount();

        double totalHarga = 0;

        for (int i = 0; i < rowCount; i++) {
            try {
                double hargaBaris = Double.parseDouble(model.getValueAt(i, 6).toString()); // Menggunakan indeks 6 untuk kolom "Harga Total"
                totalHarga += hargaBaris;
            } catch (NumberFormatException e) {
                e.printStackTrace();
                // Handle kesalahan konversi, misalnya dengan menampilkan pesan kesalahan
            }
        }

        txt_totalHarga.setText(String.valueOf(totalHarga));
    }

